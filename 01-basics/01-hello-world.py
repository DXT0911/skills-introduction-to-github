# -------------------------------------------------------
# 3. print() 函数的常见用法
# -------------------------------------------------------
print("Python 很好学！")
print(42)                       # 输出整数
print(3.14)                     # 输出浮点数
print(True)                     # 输出布尔值

# 输出多个值，用逗号分隔，默认以空格分隔
print("年龄：", 18, "岁")       # 输出：年龄：  18 岁

# 使用 sep 参数修改分隔符
print("apple", "banana", "cherry", sep=", ")  # 输出：apple, banana, cherry

# 使用 end 参数修改结尾字符（默认是换行 \n）
print("Hello", end=" ")
print("World")                  # 在同一行输出：Hello World

# -------------------------------------------------------
# 4. 缩进
# -------------------------------------------------------
# Python 使用缩进来表示代码块，通常是 4 个空格
# 缩进是强制性的，不能随意增减
if True:
    print("这行代码有 4 个空格缩进")  # 属于 if 代码块

# -------------------------------------------------------
# 5. 基本输入
# -------------------------------------------------------
# input() 函数用于从用户获取输入（注释掉，避免程序等待输入）
# name = input("请输入你的名字：")
# print("你好，" + name + "！")

# 演示用固定值代替
name = "小明"
print("你好，" + name + "！")   # 输出：你好，小明！

