# =============================================================
# 第2节：变量与数据类型
# =============================================================
# 变量是存储数据的容器。Python 是动态类型语言，
# 不需要事先声明变量类型。
# =============================================================

# -------------------------------------------------------
# 1. 变量赋值
# -------------------------------------------------------
name = "Alice"       # 字符串类型
age = 25             # 整数类型
height = 1.68        # 浮点数类型
is_student = True    # 布尔类型

print(name)          # 输出：Alice
print(age)           # 输出：25
print(height)        # 输出：1.68
print(is_student)    # 输出：True

# -------------------------------------------------------
# 2. 数据类型概览
# -------------------------------------------------------
# Python 内置的基本数据类型：
# int     - 整数
# float   - 浮点数（小数）
# str     - 字符串
# bool    - 布尔值（True / False）
# NoneType- 空值（None）

print("\n--- 数据类型 ---")
print(type(42))          # <class 'int'>
print(type(3.14))        # <class 'float'>
print(type("hello"))     # <class 'str'>
print(type(True))        # <class 'bool'>
print(type(None))        # <class 'NoneType'>

# -------------------------------------------------------
# 3. 整数（int）
# -------------------------------------------------------
print("\n--- 整数 ---")
a = 10
b = -5
c = 0
big_number = 1_000_000    # 下划线可用于分隔大数字，提高可读性

print(a, b, c, big_number)

# 不同进制表示
binary = 0b1010           # 二进制，值为 10
octal = 0o12              # 八进制，值为 10
hexadecimal = 0xA         # 十六进制，值为 10
print(binary, octal, hexadecimal)  # 输出：10 10 10

# -------------------------------------------------------
# 4. 浮点数（float）
# -------------------------------------------------------
print("\n--- 浮点数 ---")
pi = 3.14159
small = 1.5e-3            # 科学计数法，等于 0.0015
large = 2.5e6             # 等于 2500000.0

print(pi, small, large)

# 注意浮点数精度问题
print(0.1 + 0.2)          # 输出：0.30000000000000004（浮点数精度问题）
print(round(0.1 + 0.2, 2))  # 使用 round() 四舍五入解决：0.3

# -------------------------------------------------------
# 5. 字符串（str）
# -------------------------------------------------------
print("\n--- 字符串 ---")
str1 = "双引号字符串"
str2 = '单引号字符串'
str3 = """
多行
字符串
"""
print(str1)
print(str2)
print(str3)

# 字符串长度
print(len("Hello"))       # 输出：5
print(len("你好世界"))    # 输出：4

# -------------------------------------------------------
# 6. 布尔值（bool）
# -------------------------------------------------------
print("\n--- 布尔值 ---")
is_raining = False
is_sunny = True
print(is_raining)         # 输出：False
print(is_sunny)           # 输出：True

# 布尔值本质是整数
print(True + True)        # 输出：2
print(True + 1)           # 输出：2
print(False + 1)          # 输出：1

# -------------------------------------------------------
# 7. 类型转换
# -------------------------------------------------------
print("\n--- 类型转换 ---")
# 字符串转整数
num_str = "123"
num_int = int(num_str)
print(num_int, type(num_int))   # 123 <class 'int'>

# 整数转字符串
n = 456
n_str = str(n)
print(n_str, type(n_str))       # 456 <class 'str'>

# 整数转浮点数
x = float(7)
print(x)                        # 7.0

# 浮点数转整数（截断小数部分，不四舍五入）
y = int(3.99)
print(y)                        # 3

# 字符串转布尔值
print(bool(""))                 # False（空字符串为假）
print(bool("hello"))            # True（非空字符串为真）
print(bool(0))                  # False（0 为假）
print(bool(42))                 # True（非零数为真）

# -------------------------------------------------------
# 8. 变量命名规则
# -------------------------------------------------------
# 合法命名：
my_name = "Bob"
myName = "Alice"       # 驼峰命名（Python 不推荐，但合法）
_private = "私有变量"   # 以下划线开头
MAX_SIZE = 100          # 常量（全大写，约定俗成）

# 不合法命名（以下代码若取消注释会报错）：
# 2name = "error"       # 不能以数字开头
# my-name = "error"     # 不能包含连字符
# class = "error"       # 不能是 Python 关键字

print(my_name, _private, MAX_SIZE)

# -------------------------------------------------------
# 9. 多重赋值
# -------------------------------------------------------
print("\n--- 多重赋值 ---")
x, y, z = 1, 2, 3        # 同时给多个变量赋值
print(x, y, z)            # 输出：1 2 3

a = b = c = 0             # 所有变量赋同一个值
print(a, b, c)            # 输出：0 0 0

# 交换两个变量的值
x, y = 10, 20
x, y = y, x               # Python 特有的交换方式
print(x, y)               # 输出：20 10

