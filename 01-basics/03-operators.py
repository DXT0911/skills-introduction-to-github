# 运算符用于执行各种运算：算术、比较、逻辑、赋值等。
# =============================================================

# -------------------------------------------------------
# 1. 算术运算符
# -------------------------------------------------------
print("--- 算术运算符 ---")
a, b = 10, 3

print(a + b)    # 加法：13
print(a - b)    # 减法：7
print(a * b)    # 乘法：30
print(a / b)    # 除法：3.3333...（结果是浮点数）
print(a // b)   # 整除：3（向下取整）
print(a % b)    # 取余/取模：1
print(a ** b)   # 幂运算：1000（10的3次方）

# 注意除法
print(10 / 2)   # 5.0（结果是浮点数，即使能整除）
print(10 // 3)  # 3（向下取整）
print(-10 // 3) # -4（向下取整，不是截断）

# -------------------------------------------------------
# 2. 比较运算符（结果为 True 或 False）
# -------------------------------------------------------
print("\n--- 比较运算符 ---")
x, y = 5, 8

print(x == y)   # 等于：False
print(x != y)   # 不等于：True
print(x > y)    # 大于：False
print(x < y)    # 小于：True
print(x >= y)   # 大于等于：False
print(x <= y)   # 小于等于：True

# 字符串比较（按字典顺序）
print("apple" == "apple")   # True
print("apple" < "banana")   # True（'a' < 'b'）

# -------------------------------------------------------
# 3. 逻辑运算符
# -------------------------------------------------------
print("\n--- 逻辑运算符 ---")
p, q = True, False

print(p and q)   # 与：两者都为真才为真 → False
print(p or q)    # 或：至少一个为真就为真 → True
print(not p)     # 非：取反 → False
print(not q)     # 取反 → True

# 短路求值
# and：若第一个为假，直接返回第一个值（不再计算第二个）
# or：若第一个为真，直接返回第一个值（不再计算第二个）
print(0 and 1)   # 0（因为 0 为假，直接返回）
print(1 and 2)   # 2（两者都为真，返回最后一个）
print(0 or 1)    # 1（0 为假，返回第二个）
print(1 or 0)    # 1（1 为真，直接返回）

# -------------------------------------------------------
# 4. 赋值运算符
# -------------------------------------------------------
print("\n--- 赋值运算符 ---")
n = 10
print(n)     # 10

n += 5       # 等价于 n = n + 5
print(n)     # 15

n -= 3       # 等价于 n = n - 3
print(n)     # 12

n *= 2       # 等价于 n = n * 2
print(n)     # 24

n //= 5      # 等价于 n = n // 5
print(n)     # 4

n **= 3      # 等价于 n = n ** 3
print(n)     # 64

n %= 10      # 等价于 n = n % 10
print(n)     # 4

# -------------------------------------------------------
# 5. 位运算符
# -------------------------------------------------------
print("\n--- 位运算符 ---")
a, b = 0b1010, 0b1100   # a=10, b=12

print(bin(a & b))    # 按位与：0b1000 (8)
print(bin(a | b))    # 按位或：0b1110 (14)
print(bin(a ^ b))    # 按位异或：0b0110 (6)
print(bin(~a))       # 按位取反：-0b1011 (-11)
print(bin(a << 1))   # 左移1位：0b10100 (20)
print(bin(a >> 1))   # 右移1位：0b101 (5)

# -------------------------------------------------------
# 6. 成员运算符
# -------------------------------------------------------
print("\n--- 成员运算符 ---")
fruits = ["apple", "banana", "cherry"]

print("apple" in fruits)          # True
print("mango" in fruits)          # False
print("mango" not in fruits)      # True

# 字符串中的成员检查
sentence = "Hello, Python!"
print("Python" in sentence)       # True
print("Java" not in sentence)     # True

# -------------------------------------------------------
# 7. 身份运算符
# -------------------------------------------------------
print("\n--- 身份运算符 ---")
a = [1, 2, 3]
b = [1, 2, 3]
c = a

print(a is c)        # True（c 和 a 指向同一个对象）
print(a is b)        # False（a 和 b 是不同对象，即使值相同）
print(a == b)        # True（值相同）
print(a is not b)    # True

# -------------------------------------------------------
# 8. 运算符优先级（由高到低）
# -------------------------------------------------------
print("\n--- 运算符优先级 ---")
# ** > 一元+ - > * / // % > + - > 比较 > not > and > or
result = 2 + 3 * 4 ** 2     # 先算 4**2=16，再 3*16=48，再 2+48=50
print(result)                # 50

result = (2 + 3) * 4         # 括号优先
print(result)                # 20

